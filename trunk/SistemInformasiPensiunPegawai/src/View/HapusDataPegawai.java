    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            List<PNS> search = ControlData.getKoneksi().SearchDataPNS_NIPBaru(cari_txt.getText());
            if (cari_txt.getText().equalsIgnoreCase("")) {
                inputKosong();
            } else if (cari_txt.getDocument().getLength() < 18) {
                JOptionPane.showMessageDialog(rootPane, "NIP Pegawai tidak Lengkap", "perhatian", JOptionPane.WARNING_MESSAGE);
            } else if (search.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "data Pegawai tidak ditemukan");
            } else if (!search.isEmpty()) {
                for (PNS k : search) {
                    int pilihan = JOptionPane.showConfirmDialog(rootPane, "Data ditemukan \n"
                            + "dengan NIP " + k.getNip_baru() + " \n atas nama " + k.getNama_pns() + "\n"
                            + "Apakah akan lihat daftar surat ? ", "konfirmasi", JOptionPane.OK_CANCEL_OPTION);
                    if (pilihan == 0) {
                        refreshListComboSurat(cari_txt.getText());
                    }
                }
            }
        } catch (Exception e) {
        }

        // TODO add your handling code here:
}//GEN-LAST:event_jButton1ActionPerformed

