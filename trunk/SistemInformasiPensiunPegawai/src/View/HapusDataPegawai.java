    public Date tukangSplit(String tanggal) throws ParseException {
        String[] split = tanggal.split("-");
        String formatBener = split[2] + "-" + split[1] + "-" + split[0];
        Date kirimFormat = new SimpleDateFormat("dd-MM-yyyy").parse(formatBener);
        return kirimFormat;
    }

