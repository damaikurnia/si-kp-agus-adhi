//                        }
                        Map reportparametermap1 = new HashMap();

                        reportparametermap1.put("NIP", nip_TF.getText());
                        String reportSource = "./Cetak/CoverBUP.jasper";
                        String reportSource2 = "./Cetak/Surat_keterangan.jasper";
